class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
    sort(a.begin(),a.end());
    
    long long mn=INT_MAX;
    for(long long i=0;i<=n-m;i++)
    {
    long long a1=a[i];
    long long a2=a[i+m-1];
    long long a3=a2-a1;
    
        if(a3<mn)
        {
            
        mn=a3;
        }
    }
    return mn;
    }   
};

